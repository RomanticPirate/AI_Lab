const 상성표 = { '주먹': '날라차기', '발차기': '주먹', '날라차기': '발차기' };
const 액션들 = ['주먹', '발차기', '날라차기'];
const 더블매핑 = { '주먹': 1, '발차기': 2, '날라차기': 3 };

function 스킬보유(펫, id) { return 펫.스킬1 === id || 펫.스킬2 === id; }
function 확률(p) { return Math.random() * 100 < p; }

function 전투(A, B, o) {
    let hpA = A.HP, hpB = B.HP;
    const mA = A.HP, mB = B.HP;
    let zA = false, zB = false;

    for (let r = 0; r < 100; r++) {
        if (스킬보유(A, 10) && 확률(o.치유확률) && hpA < mA) hpA = Math.min(mA, hpA + o.치유량);
        if (스킬보유(B, 10) && 확률(o.치유확률) && hpB < mB) hpB = Math.min(mB, hpB + o.치유량);

        const sA = 액션들[Math.floor(Math.random() * 3)];
        const sB = 액션들[Math.floor(Math.random() * 3)];
        if (sA === sB) continue;

        const aw = 상성표[sA] === sB;
        const 공 = aw ? A : B, 방 = aw ? B : A, 액 = aw ? sA : sB;
        let 공h = aw ? hpA : hpB, 방h = aw ? hpB : hpA;
        const 공m = aw ? mA : mB;

        let d = 액 === '주먹' ? 공.주먹대미지 : 액 === '발차기' ? 공.발차기대미지 : 공.날라차기대미지;
        const did = 더블매핑[액];
        if (did && 스킬보유(공, did)) d *= o.더블배율;
        if (스킬보유(공, 7) && 공h <= 공m * 0.3 && 확률(o.분노확률)) d *= o.분노배율;
        if (스킬보유(방, 4)) d = Math.max(1, d - o.철벽감소);
        방h = Math.max(0, 방h - d);
        if (스킬보유(공, 5)) 공h = Math.min(공m, 공h + o.흡혈량);
        if (스킬보유(공, 8) && 확률(o.연속타확률)) 방h = Math.max(0, 방h - o.연속타대미지);
        if (스킬보유(방, 9) && 확률(o.카운터확률)) 공h = Math.max(0, 공h - o.카운터대미지);

        if (aw) { hpA = 공h; hpB = 방h; } else { hpB = 공h; hpA = 방h; }

        if (hpA <= 0) { if (스킬보유(A, 6) && !zA && 확률(o.좀비확률)) { zA = true; hpA = o.좀비HP; } else return 'B'; }
        if (hpB <= 0) { if (스킬보유(B, 6) && !zB && 확률(o.좀비확률)) { zB = true; hpB = o.좀비HP; } else return 'A'; }
        if (hpA <= 0) { if (스킬보유(A, 6) && !zA && 확률(o.좀비확률)) { zA = true; hpA = o.좀비HP; } else return 'B'; }
    }
    return 'draw';
}

function 시뮬(펫들, o, n = 5000) {
    const R = {}; 펫들.forEach(p => { R[p.이름] = { 승: 0, 패: 0, 무: 0 }; });
    const M = [];
    for (let i = 0; i < 펫들.length; i++) for (let j = i+1; j < 펫들.length; j++) {
        let a=0,b=0,d=0;
        for (let k=0;k<n;k++){const r=전투(펫들[i],펫들[j],o);if(r==='A')a++;else if(r==='B')b++;else d++;}
        R[펫들[i].이름].승+=a; R[펫들[i].이름].패+=b; R[펫들[i].이름].무+=d;
        R[펫들[j].이름].승+=b; R[펫들[j].이름].패+=a; R[펫들[j].이름].무+=d;
        M.push({A:펫들[i].이름,B:펫들[j].이름,a,b,d});
    }
    return {R,M};
}

